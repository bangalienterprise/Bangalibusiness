import { serve } from "https://deno.land/std@0.168.0/http/server.ts"

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
}

serve(async (req) => {
  if (req.method === 'OPTIONS') {
    return new Response('ok', { headers: corsHeaders })
  }

  try {
    const { name } = await req.json()
    
    // Logic: Check SQL safety (Mock)
    // In a real function, this would analyze queries or check permissions
    const isSafe = !name.includes("DROP TABLE") && !name.includes("DELETE");

    const data = {
      message: isSafe ? `Query appears safe` : `Potential SQL Injection detected`,
      safety_score: isSafe ? 100 : 0,
      input_analyzed: name,
    }

    return new Response(JSON.stringify(data), {
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
      status: 200,
    })
  } catch (error) {
    return new Response(JSON.stringify({ error: error.message }), {
      headers: { ...corsHeaders, 'Content-Type': 'application/json' },
      status: 400,
    })
  }
})
